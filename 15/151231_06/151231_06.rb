def A001422()
  # 145以上の自然数は異なる四角数の和で表されることが証明できているものとする
  ary = (1..12).map{|i| i * i}
  a_ary = []
  (1..ary.size).each{|i|
    ary.combination(i){|c|
      a_ary << c.inject(:+)
    }
  }
  (1..144).to_a - a_ary.uniq
end
ary = A001422()

